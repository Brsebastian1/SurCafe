    public void agregarManoObra(){
         String nombre = txtTrabajador.getText();
        String salario = txtSalario.getText();
        int suma = 0;
        txtTotalInsumos.setText(txtTotalInsumos.getText());
        if (txtTrabajador.getText().length() > 0) {
            if(txtSalario.getText().length()>0){
            modeloTrabajador.addElement(nombre);
            modeloPagos.addElement(salario);
            arrayPagos.add(salario);
        }
    }

        for (int i = 0; i < arrayPagos.size(); i++) {
            suma += Integer.parseInt(modeloPagos.getElementAt(i).toString());
            txtTotalManoObra.setText(String.valueOf(suma));

        }

        txtTrabajador.setText("");
        txtSalario.setText("");
    }
    
    public void agregarInsumos(){
        String insumo = txtInsumo.getText();
        String valor = txtValor.getText();
        int suma = 0;
        txtTotalManoObra.setText(txtTotalManoObra.getText());

        if (txtInsumo.getText().length() > 0) {
            if(txtValor.getText().length()>0){
            modeloInsumos.addElement(insumo);
            modeloValores.addElement(valor);
            arrayValores.add(valor);
        }
        }

        for (int i = 0; i < arrayValores.size(); i++) {
            suma += Integer.parseInt(modeloValores.getElementAt(i).toString());
            txtTotalInsumos.setText(String.valueOf(suma));

            txtInsumo.setText("");
            txtValor.setText("");
        }
    }
    
    public void limpiar(){
        modeloTrabajador.removeAllElements();
        modeloPagos.removeAllElements();
        modeloInsumos.removeAllElements();
        modeloValores.removeAllElements();
        txtTotalManoObra.setText(String.valueOf(0));
        txtTotalInsumos.setText(String.valueOf(0));
        txtTotalGastos.setText(String.valueOf(0));
        txtTotalRecoleccion.setText(String.valueOf(0));
        
        for(int i=0; i<arrayPagos.size(); i++){
            arrayPagos.remove(i);
        }
        
        for(int i=0; i<arrayValores.size(); i++){
            arrayValores.remove(i);
        }
    }
    
    public void eliminar(){
        if(jListTrabajadores.getSelectedIndex()!=-1){
            int eliminar = jListTrabajadores.getSelectedIndex();
            modeloTrabajador.remove(eliminar);
            modeloPagos.remove(eliminar);
            arrayPagos.remove(eliminar);
        }else if(jListInsumos.getSelectedIndex() != -1){
            int eliminar = jListInsumos.getSelectedIndex();
            modeloInsumos.remove(eliminar);
            modeloValores.remove(eliminar);
            arrayValores.remove(eliminar);
        }
        
        int suma=0;
        if(!arrayPagos.isEmpty()){
            for(int i=0; i<arrayPagos.size(); i++){
            suma += Integer.parseInt(modeloPagos.getElementAt(i).toString());
            txtTotalManoObra.setText(""+suma);
            }
        }else{
            txtTotalManoObra.setText(String.valueOf(0));
        }
        
        if(!arrayValores.isEmpty()){
            suma=0;
            for(int i=0; i<arrayValores.size(); i++){
                suma += Integer.parseInt(modeloValores.getElementAt(i).toString());
                txtTotalInsumos.setText(""+suma);

                txtInsumo.setText("");
                txtValor.setText("");
            }
        }else{
            txtTotalInsumos.setText(String.valueOf(0));
        }
    }
    
    public void sumar(){
        int total;
        total = Integer.parseInt(txtTotalManoObra.getText())+Integer.parseInt(txtTotalInsumos.getText()+txtTotalRecoleccion.getText());
        String valor = String.valueOf(total);
        txtTotalGastos.setText(valor);
    }
   
    
    
