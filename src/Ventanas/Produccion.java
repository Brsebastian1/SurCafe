    public void recolectado(){
        String kilos = txtCafe.getText();
        int suma=0;
        
        if(txtCafe.getText().length()>0){
            modeloProduccion.addElement(kilos);
            arrayProduccion.add(kilos);
            
            if(!arrayProduccion.isEmpty()){
                for(int i=0; i<arrayProduccion.size(); i++){
                    suma += Integer.parseInt(modeloProduccion.getElementAt(i).toString());
                    txtTotalCafe.setText(String.valueOf(suma));
                }
            }
        }
        txtCafe.setText("");
    }
    
    public void eliminar(){
        int suma=0;
        
        if(jListCafe.getSelectedIndex()>-1){
            int eliminar = jListCafe.getSelectedIndex();
            modeloProduccion.remove(eliminar);
            arrayProduccion.remove(eliminar);
        }
        
        if(!arrayProduccion.isEmpty()){
            for(int i=0; i<arrayProduccion.size(); i++){
                suma += Integer.parseInt(modeloProduccion.getElementAt(i).toString());
                txtTotalCafe.setText(String.valueOf(suma));
            }
        }else{
            txtTotalCafe.setText(String.valueOf(0));
        }
    }

    
     public void calcular(){
        int Kg = Integer.parseInt(txtIngresarSeco.getText());
        int precio = Integer.parseInt(txtPrecio.getText());
        int valor;
        
        valor = (precio/125)*Kg;
        txtValorTotal.setText(String.valueOf(valor));
        
        txtIngresarSeco.setText("");
        txtPrecio.setText("");
    }
    
    public void aproximado(){
        int cafeGrano = Integer.parseInt(txtTotalCafe.getText());
        int aproximado;
        
        aproximado = (cafeGrano*125)/600;
        
        txtAproximado.setText(String.valueOf(aproximado));
    }
    
